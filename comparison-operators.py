# = is for assigning values to a var 

# == checking the equality, equal to 
print('\n Equality check results:')
print(2 == 2)
print('Hello' == 'Bye')
print('Hello' == 'hello')
print('2' == 2)

# != checking NOT equal to 
print('\nNOT equal to results:')
print(3 != 4)
print(3 != 3)

# greater than or less than 
print('\nGreater than and less than results:')
print(2 > 3)
print(2 < 3)
print(100 <= 100)
print(100 >= 100)

# We can use logical operators to combine comparisons - 'and', 'or', and 'not'
# and - both sides need to be true to get boolean TRUE 
print('\nLogical operators results:')
print(1 < 2 and 2 < 3)

# or - only one side needs to be true to get boolean TRUE 
print(1 == 1 or 2 < 3)

# not - to get the opposite boolean value 
print(not 1 == 1)
